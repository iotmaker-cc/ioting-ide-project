jong = [ 0, 0, 2, 0, 2, 1, 2, 1, 2, 3, 0, 2, 1, 3, 3, 1, 2, 1, 3, 3, 1, 1]

def draw_hangul(oled, text, x, y,mode=None):
    """텍스트를 OLED에 그리는 함수 (각 글자 16x16)"""
    
    v_count = 1
    r_count = 1
    
    if mode:
        mode = mode.lower()
    
    if mode == 'v2':
        v_count = 2
    elif mode == 'h2':
        r_count = 2
    elif mode == 'x4':
        v_count = 2
        r_count = 2
    
    for char in text:
        if ord(char) >= 128: # 한글
            draw_hg(oled, char, x, y, v_count,r_count)
            x += 16 * r_count  # 다음 글자 위치로 이동 (글자 간격 조정)
        else: # ascii char
            draw_asc(oled, char, x, y, v_count,r_count)
            x += 8  * r_count # 다음 글자 위치로 이동 (글자 간격 조정)
            

def draw_hg(oled, char, x, y, v_count, r_count):
    """16x16 비트맵 글자를 OLED에 그리는 함수"""

    bitmap = hg_img(char)
                
    row1 = 0
    for row in range(16):
        byte1 = bitmap[row * 2]
        byte2 = bitmap[row * 2 + 1]
        
        for v in range(v_count):
            col1 = 0
            for col in range(16):
                for r in range(r_count):
                    if col < 8:
                        bit = (byte1 >> (7 - col)) & 0x01
                    else:
                        bit = (byte2 >> (15 - col)) & 0x01
                    oled.pixel(x + col1, y + row1, bit)
                    col1 += 1
              
            row1 += 1
            
def draw_asc(oled, asc, x, y, v_count,r_count):
    """8x16 비트맵 글자를 OLED에 그리는 함수"""

    bitmap = asc_img(asc)
    
    row1 = 0
    for row in range(16):
        for v in range(v_count):
            byte1 = bitmap[row]
            #byte2 = bitmap[row * 2 + 1]
            col1 = 0
            for col in range(8):
                for r in range(r_count):
                    bit = (byte1 >> (7 - col)) & 0x01
                    oled.pixel(x + col1, y + row1, bit)
                    col1 += 1
            row1 += 1

def asc_img(asc):
    
    p = (ord(asc) - 0x20) * 16;
      
    asc = [0]*16
    for i in range(0,16):
        asc[i] = ASCFont[p+i]
    
    return asc

def hg_img(char):
    
    unicode = ord(char)
    utf16 = unicode - 0xac00  # '가': 0xac00
    last = int(utf16 % 28)
    utf16 /= 28
    mid = int(utf16 % 21)
    first = int(utf16 / 21)
    
    first += 1
    mid += 1
    
    lastType = last
    
    if not last: # //받침 없는 경우
        firstType = cho[mid]
        if(first == 1 or first == 24):
            midType = 0;
        else:
            midType = 1;
    
    else: # //받침 있는 경우
        firstType = cho2[mid]
        if(first == 1 or first == 24):
            midType = 2
        else:
            midType = 3
        lastType = jong[mid]
        
    p1 = (firstType * 20 + first)*32
    p2 = (8*20 + midType*22 + mid)*32
    p3 = (8*20 + 4*22 + lastType*28 + last)*32
    
    hg = [0]*32
    for i in range(32):
        hg[i] = KSFont[p1+i] | KSFont[p2+i] | KSFont[p3+i]
    
    return hg

