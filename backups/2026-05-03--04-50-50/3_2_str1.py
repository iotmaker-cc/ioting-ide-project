# 문자열 묶는 방법
f = "hong"  # ""로 묶기
n = '길동'  # ''로 묶기

# 문자열 연결하기 
fn = f + n     # hong길동

# 글자수 확인
ln = len(fn) # 6

# '와 " 출력하기 
qt1 = "It's beautiful day"      # It' beautiful day
qt2 = 'He said,"Hello World!"'  # He said,"Hello World!"

print(1,fn)
print(2,ln)
print(3,qt1)
print(4,qt2)