a = 3
b = 5

# AND 연산자
if a > 0 and b < 10:
    # 이 조건은 참이므로 출력됩니다.
    print("a는 0보다 크고, b는 10보다 작습니다.")  

# OR 연산자
if a < 0 or b > 3:
    # b > 3이므로 이 조건은 참입니다.
    print("a는 0보다 작거나, b는 3보다 큽니다.")  

# NOT 연산자
if not (a == b):
    # a와 b는 같지 않으므로 출력됩니다.
    print("a와 b는 같지 않습니다.")  